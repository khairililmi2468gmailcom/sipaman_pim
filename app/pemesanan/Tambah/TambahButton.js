import React, { useState } from 'react';
import TambahModal from './TambahModal';

const TambahButton = ({ onCreateSuccesss }) => {
    const [isModalOpen, setIsModalOpen] = useState(false);

    const toggleModal = () => {
        setIsModalOpen(!isModalOpen);
    };

    return (
        <>
            <button
                type="button"
                onClick={toggleModal}
                id="tambahPesanan"
                data-modal-toggle="tambahPesanan"
                class="flex items-center justify-center text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:ring-primary-300 font-medium rounded-lg text-sm px-4 py-2 dark:bg-primary-600 dark:hover:bg-primary-700 focus:outline-none dark:focus:ring-primary-800"            >
                <svg class="h-3.5 w-3.5 mr-1.5 -ml-1" fill="currentColor" viewbox="0 0 20 20" xmlns="http://www.w3.org/2000/svg" aria-hidden="true">
                    <path clip-rule="evenodd" fill-rule="evenodd" d="M10 3a1 1 0 011 1v5h5a1 1 0 110 2h-5v5a1 1 0 11-2 0v-5H4a1 1 0 110-2h5V4a1 1 0 011-1z" />
                </svg>
                Tambah Pesanan
            </button>
            {isModalOpen && <TambahModal toggleModal={toggleModal} onCreateSuccess={onCreateSuccesss} />}
        </>
    );
}

export default TambahButton;
